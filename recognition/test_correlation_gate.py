import sys
import os
sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))

import numpy as np


# ?????????????????????????????????????????????????????
# CONSTANTS (mirrors RegistrationHandler)
# ?????????????????????????????????????????????????????

RE_ENROLL_CORRELATION_MIN = 0.70
RE_ENROLL_BLEND_RATIO = 0.30


# ?????????????????????????????????????????????????????
# RE-ENROLL LOGIC (mirrors RegistrationHandler.re_enroll)
# ?????????????????????????????????????????????????????

def verify_re_enrollment(
    existing_vector: np.ndarray,
    new_centroid: np.ndarray,
) -> tuple:
    """
    Check correlation and perform EMA blend.

    Returns:
        (allowed: bool, correlation: float, blended: Optional[np.ndarray])
    """
    correlation = float(np.dot(existing_vector, new_centroid))

    if correlation < RE_ENROLL_CORRELATION_MIN:
        return False, correlation, None

    blended = (
        (1.0 - RE_ENROLL_BLEND_RATIO) * existing_vector +
        RE_ENROLL_BLEND_RATIO * new_centroid
    )
    blended = blended / np.linalg.norm(blended)

    return True, correlation, blended


# ?????????????????????????????????????????????????????
# HELPER
# ?????????????????????????????????????????????????????

def _unit_vector(dim: int = 512, seed: int = None) -> np.ndarray:
    if seed is not None:
        np.random.seed(seed)
    v = np.random.randn(dim).astype(np.float32)
    return v / np.linalg.norm(v)


# ?????????????????????????????????????????????????????
# TEST 5.1: Identity Hijacking Attempt
# ?????????????????????????????????????????????????????

def test_identity_hijacking():
    """
    Attempt to overwrite User A with User B's face.
    Expected: correlation < 0.70, IdentityHijackError.
    """
    print("\n" + "=" * 60)
    print("TEST 5.1: Identity Hijacking Attempt")
    print("=" * 60)

    np.random.seed(42)
    user_a = _unit_vector(seed=100)
    user_b = _unit_vector(seed=200)

    allowed, correlation, blended = verify_re_enrollment(user_a, user_b)

    print(f"  User A ? User B correlation: {correlation:.4f}")
    print(f"  Threshold: {RE_ENROLL_CORRELATION_MIN}")
    print(f"  Re-enrollment allowed: {allowed}")

    assert not allowed, \
        f"Correlation {correlation:.4f} >= {RE_ENROLL_CORRELATION_MIN}: hijack would succeed!"
    assert correlation < RE_ENROLL_CORRELATION_MIN, \
        f"Correlation {correlation:.4f} should be below {RE_ENROLL_CORRELATION_MIN}"
    assert blended is None, "Blended vector should not be computed"

    print("  [PASS] Hijacking attempt correctly rejected")
    return True


# ?????????????????????????????????????????????????????
# TEST 5.2: Valid Re-enrollment
# ?????????????????????????????????????????????????????

def test_valid_re_enrollment():
    """
    Overwrite User A with a newer, high-quality scan of User A.
    Expected: correlation > 0.70, blended vector on unit sphere.
    """
    print("\n" + "=" * 60)
    print("TEST 5.2: Valid Re-enrollment")
    print("=" * 60)

    np.random.seed(7)
    base = _unit_vector()

    # Original enrollment (slightly noisy)
    old = base + np.random.randn(512).astype(np.float32) * 0.05
    old = old / np.linalg.norm(old)

    # New scan (different noise, same identity)
    new = base + np.random.randn(512).astype(np.float32) * 0.10
    new = new / np.linalg.norm(new)

    allowed, correlation, blended = verify_re_enrollment(old, new)

    print(f"  Old ? New correlation: {correlation:.4f}")
    print(f"  Threshold: {RE_ENROLL_CORRELATION_MIN}")
    print(f"  Re-enrollment allowed: {allowed}")

    assert allowed, \
        f"Correlation {correlation:.4f} >= {RE_ENROLL_CORRELATION_MIN}: should be allowed"
    assert correlation >= RE_ENROLL_CORRELATION_MIN, \
        f"Correlation {correlation:.4f} should meet threshold {RE_ENROLL_CORRELATION_MIN}"
    assert blended is not None, "Blended vector should be computed"

    # Verify blended vector properties
    blend_norm = float(np.linalg.norm(blended))
    assert abs(blend_norm - 1.0) < 1e-6, \
        f"Blended norm = {blend_norm:.8f}, expected 1.0"

    blend_old_sim = float(np.dot(blended, old))
    blend_new_sim = float(np.dot(blended, new))

    print(f"  Blended norm: {blend_norm:.8f}")
    print(f"  Blended ? Old similarity: {blend_old_sim:.4f}")
    print(f"  Blended ? New similarity: {blend_new_sim:.4f}")

    assert blend_old_sim > 0.70, \
        f"Blended should retain {RE_ENROLL_BLEND_RATIO*100:.0f}% old identity"

    print("  [PASS] Valid re-enrollment accepted and blended correctly")
    return True


